fplot(@(f) 1/sqrt((1/R)^2+(2*pi*f*C-1/(2*pi*f*L))^2),[fr-50 fr+100]);
xlabel('Frequency(Hz)-------->');
ylabel('Impedance(ohm)--------->');
title('Z v\s f');
subplot(3,2,2);
% Vm=Im*Z
fplot(@(f) 10/sqrt((1/R)^2+(2*pi*f*C-1/(2*pi*f*L))^2),[fr-50 fr+100]);
xlabel('Frequency(Hz)-------->');
ylabel('Max voltage(V)--------->');
title('Vm v\s f');
subplot(3,2,3);
% Vrms=Irms*Z
fplot(@(f) (10/sqrt(2))/sqrt((1/R)^2+(2*pi*f*C-1/(2*pi*f*L))^2),[fr-50 fr+100]);
xlabel('Frequency(Hz)-------->');
ylabel('Rms voltage(V)--------->');
title('Vrms v\s f');
subplot(3,2,4);
